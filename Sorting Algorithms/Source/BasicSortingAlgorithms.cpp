void bubbleSort(std::vector<int>& v)
{
	for(std::size_t i = 0; i < v.size() - 1; ++i)
	{
		for(std::size_t j = 0; j < v.size() - 1 - i; ++j)
		{
			// If the elements are in wrong order then swap them
			if(v[j + 1] < v[j])
				std::swap(v[j],v[j + 1]);
		}
	}
}